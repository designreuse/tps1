public class ExtraBedModel {
    private String extraBedId, hotelDetailId, noOfExtraBed,
            infantRate, childAgeMax, childRate, adultRate, active;

    public String getExtraBedId() {
        return extraBedId;
    }

    public void setExtraBedId(String extraBedId) {
        this.extraBedId = extraBedId;
    }

    public String getHotelDetailId() {
        return hotelDetailId;
    }

    public void setHotelDetailId(String hotelDetailId) {
        this.hotelDetailId = hotelDetailId;
    }

    public String getNoOfExtraBed() {
        return noOfExtraBed;
    }

    public void setNoOfExtraBed(String noOfExtraBed) {
        this.noOfExtraBed = noOfExtraBed;
    }

    public String getInfantRate() {
        return infantRate;
    }

    public void setInfantRate(String infantRate) {
        this.infantRate = infantRate;
    }

    public String getChildAgeMax() {
        return childAgeMax;
    }

    public void setChildAgeMax(String childAgeMax) {
        this.childAgeMax = childAgeMax;
    }

    public String getChildRate() {
        return childRate;
    }

    public void setChildRate(String childRate) {
        this.childRate = childRate;
    }

    public String getAdultRate() {
        return adultRate;
    }

    public void setAdultRate(String adultRate) {
        this.adultRate = adultRate;
    }

    public String getActive() {
        return active;
    }

    public void setActive(String active) {
        this.active = active;
    }
}
