package igc.tech.com.model;

public class AvailableDateRateModel {


    private String availableDateRateId;
    private String availableDate;
    private String packageInfoId;
    private String packageName;
    private String rate;


    private String user;


    public String getAvailableDateRateId() {
        return availableDateRateId;
    }

    public void setAvailableDateRateId(String availableDateRateId) {
        this.availableDateRateId = availableDateRateId;
    }

    public String getAvailableDate() {
        return availableDate;
    }

    public void setAvailableDate(String availableDate) {
        this.availableDate = availableDate;
    }

    public String getPackageInfoId() {
        return packageInfoId;
    }

    public void setPackageInfoId(String packageInfoId) {
        this.packageInfoId = packageInfoId;
    }

    public String getPackageName() {
        return packageName;
    }

    public void setPackageName(String packageName) {
        this.packageName = packageName;
    }


    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }


}
